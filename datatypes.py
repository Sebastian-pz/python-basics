# Tipos de datos.

# Strings
a = "Hello world"
b = 'Hello world'
c = '''Hello world'''
print(type(a), type(b), type(c))

# Ints
a = 1
b = 2
c = 3
print(type(a), type(b), type(c))

# Floats
a = 1.3
b = 2.3
c = 3.3
print(type(a), type(b), type(c))

# Bools -> Boleans
a = True
b = False
c = True
print(type(a), type(b), type(c))

# dict -> object Python: Dictionarie
a = {"llave1": "valor3"}
b = {"llave2": "valor2"}
c = {"llave3": "valor1"}
print(type(a), type(b), type(c))

# List
a = [1,2,3]
b = ["1", "2", "3"]
c = [True, False, False]
print(type(a), type(b), type(c))

# Tuple -> Constant <-
a = (1,2,3)
b = ("1", "2", "3")
c = (True, False, False)
print(type(a), type(b), type(c))

# None
a = None
print(type(a))
